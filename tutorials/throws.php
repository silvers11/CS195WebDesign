<h3>Forehand "Flick"</h3>
<p> The Forehand throw is tricky to learn but absolutely necessary for all Ultiamte players to know.</p>

<h3>Hammer</h3>
<p>The Hammer throw is useful in certain situations, however, it is considered a low percentage shot and shhould only be used when needed.</p>

